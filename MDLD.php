            <h2>BIENVENUE</h2>
            <p>La communication est une nécessité pour toute institution publique. Le secteur de la santé est un domaine sensible pour lequel la bonne circulation de l’information entre ses principaux animateurs (tutelle, professionnels de la santé, partenaires…) et les bénéficiaires est essentielle pour une meilleure prise en charge de la santé des populations.</p>
            <p>La création du Programme National de l’Activité Pharmaceutique (PNDAP) répond au souci de mise en cohérence des interventions dans le secteur pharmaceutique dans le cadre d’une politique pharmaceutique nationale (PPN), ainsi le PNDAP a pour mission de promouvoir la PPN et d’assurer la coordination de sa mise en œuvre.</p>
            <p>La mise en cohérence des interventions dans le secteur pharmaceutique est une exigence pour l’efficience du système sanitaire national, et ne saurait être réalisée sans la bonne circulation de la juste information. C’est pourquoi, conscient de l’importance du moyen de communication et d’information que pourrait constituer un site web, le PNDAP propose un site mis à jour régulièrement et avec une volonté de l’enrichir constamment.</p>
            <p>Ce site se veut être un espace de <strong>communication institutionnelle efficace entre l’ensemble des acteurs (publics ou privés, nationaux ou internationaux) du secteur pharmaceutique national</strong> à qui nous souhaitons la bienvenue. A travers ce site, tout professionnel de la santé, tout partenaire du système, mais aussi tout usager et bénéficiaire de ce système, pourrait accéder à des informations utiles concernant le secteur pharmaceutique national.</p>
            <p>Le site, tel que nous l’avons conçu, vise à mieux faire connaître le secteur pharmaceutique national et ses acteurs. A travers nos différentes pages nous vous offrons :</p>
            <ul>
                <li>la promotion des comportements favorables à la santé,</li>
                <li>la sensibilisation des populations et des professionnels de la santé à l’usage rationnel des médicaments,</li>
                <li>les efforts entrepris en faveur d’une grande accessibilité des populations à des médicaments de qualité,</li>
                <li>l’opportunité des recherches et études menées sur le secteur,</li>
                <li>l’opportunité d’avoir accès à toute structure du secteur pharmaceutique national disposant d’un site web.</li>
            </ul>
            <p>Si les raisons de votre visite sur ce site sont des préoccupations liées aux questions pharmaceutiques au niveau national, nous espérons que vous y trouverez toutes les informations recherchées.</p>

