        <h2>EDITORIAL-----</h2>
        <p>Tout citoyen a droit à la santé. Ainsi, les autorités politiques et  les professionnels de la santé doivent garantir ce droit,  surtout que  la santé  est le levier indispensable au développement d’un pays.</p>
        <p>Or,  l’accès à des soins de qualité est de plus en plus  réservé aux plus nantis. En outre du fait du coût élevé des médicaments, les patients  sans moyens  se dirigent parfois vers les marchés de vente illicite de médicaments.</p>
        <h3>Afin de remédier à cette situation, le Ministère de la Santé de l’Hygiène Publique et de la Couverture Maladie Universelle (MSHPCMU), à travers la Direction de l’Activité Pharmaceutique (DAP) et avec l’appui technique et financier des partenaires, a élaboré un Plan Stratégique Pluriannuel dénommé Plan National Stratégique de la Chaine  d’Approvisionnement (PNSCA) en produits de santé.</h3>
        <p>Ce plan vise à rendre performant la chaîne d’approvisionnement des médicaments essentiels et intrants stratégiques en Côte d’Ivoire pour assurer un accès constant des populations à des médicaments essentiels, de qualités, sûrs, efficaces, accessibles géographiquement et financièrement.</p>
        <p>Parallèlement aux volets soins et médicaments, la DAP a mis en place des moyens de communication  pour mieux informer les professionnels de santé et les populations afin qu’ils  adoptent de bonnes pratiques sanitaires</p>
        <p>Ainsi, depuis 2016, nous avons entrepris entre autres :</p>
        <h3>Au plan sanitaire :</h3>
        <ul>
          <li>L’Elaboration du Plan National Stratégique de la Chaîne d’Approvisionnement (PNSCA) en produits de santé;</li>
          <li>L’Elaboration de la Cartographie des Systèmes d’Approvisionnement et de distribution des médicaments et autres produits de santé en Côte d’Ivoire;</li>
          <li>La Mise en place d’un système de rapportage  au niveau décentralisé et élaboration du rapport trimestriel des activités de la chaîne d’approvisionnement;</li>
          <li>Le Plan d’action pays 2016-2019 du Projet Régional d’Autonomisation des Femmes et le Dividende Démographique au Sahel SWEDD 2.2 Chaîne d’approvisionnement.</li>
        </ul>
        <h3>Au plan de la communication :</h3>
        <ul>
          <li>La Rénovation du site web pour mieux servir les acteurs de la santé  et la population;</li>
          <li>L’Amélioration en termes de nombre d’exemplaires (10.000) et de pages (6) du bulletin d’information <span><a href="">(PHARMANEWS) </a></span>pour mieux rendre compte des activités du secteur;</li>
          <li>L’Amélioration de la Plate-forme d’envois réguliers de newsletters aux professionnels de la santé.</li>
        </ul>
        <h3>Perspectives :</h3>
        <p>La DAP compte pour les années prochaines relever des défis afin de permettre à la population de bénéficier d’un meilleur accès aux médicaments de qualité. Il s’agira entre autres :</p>
        <ul>
          <li>De réviser la Politique Pharmaceutique National (PPN) et son Plan Directeur (PDPN);</li>
          <li>De déployer l’application électronique Msupply visant à améliorer la gestion des stocks des produits de santé;</li>
          <li>D’assurer la formation en ligne des gestionnaires des produits de santé à travers la plateforme ECAPS.CI;</li>
          <li>D’assurer la distribution des produits de santé jusqu’au dernier kilomètre;</li>
          <li>Collaborer à la mise en place du GTT Système de Laboratoire d’Analyses de Biologie Médicale (GTT LABM).</li>
        </ul>
        <p>Nous espérons que ces moyens permettront la <strong>MARCHE VERS UN SECTEUR PHARMACEUTIQUE PLUS DYNAMIQUE.</strong></p>
